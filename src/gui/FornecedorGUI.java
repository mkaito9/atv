    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        Fornecedor fornecedores = new Fornecedor();
        fornecedores.setRazaoSocial(txtRazaoSocial.getText());
        fornecedores.setNomeFantasia(txtNomeFantasia.getText());
        fornecedores.setCnpj(txtCnpj.getText());
        fornecedores.setCidade(txtCidade.getText());
        fornecedores.setEstado(txtEstado.getText());
        
        if((txtRazaoSocial.getText().isEmpty()) || (txtNomeFantasia.getText().isEmpty()) || 
                (txtCnpj.getText().isEmpty()) || (txtCidade.getText().isEmpty()) 
                || (txtEstado.getText().isEmpty())){
            JOptionPane.showMessageDialog(null, "CAMPOS EST√ÉO VAZIOS!");
        }
        else{
            FornecedorDAO dao = new FornecedorDAO();
            dao.adicionaFornecedor(fornecedores);
            JOptionPane.showMessageDialog(null, "FORNECEDOR CADASTRADO COM SUCESSO!");
        }
        
        txtRazaoSocial.setText("");
        txtNomeFantasia.setText("");
        txtCnpj.setText("");
        txtCidade.setText("");
        txtEstado.setText("");
        
    }//GEN-LAST:event_btnCadastrarActionPerformed

